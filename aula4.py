# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer númer
#ositivo ou negativo, int sem sinal é considerado positivo

print(11) # itn
print(-63) # itn

#float -> Número de ponto flutuante (reais)
#O tipo float tem um ponto decimal que pode ser nulo ou não.  
#Existem dois tipos de floats: 
#- Float com sinal (float): possui um sinal (positivo ou negativo).
#- Float sem sinal (double): sem sinal (apenas valores positivos).

print(0.0, 1.5, -2.7) #flt

print( type(0)) # <class 'int'>
print(type(0.0)) #<class 'float'>
